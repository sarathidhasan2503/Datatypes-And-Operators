package com.datatype;

public class Datatypes {

	public static void main(String[] args) {
		byte bytemin=-128;
		byte bytemax=127;
		short shortmin=-32768;
		short shortmax=32767;
		int intmin=-2147483648;
		int intmax=2147483647;
		long Minvalue=-9223372036854775808l;
		long Maxvalue=9223372036854775807l;
		double Min=4.9E-324;
		double Max=1.7976931348623157E308;
		System.out.println("Byte Max is ="+bytemax+" And Byte Min is ="+bytemin);
		System.out.println("Short Max is ="+shortmax+" And Short Min is ="+shortmin);
		System.out.println("Int Max is ="+intmax+" And Int Min is ="+intmin);
		System.out.println("Long Max is ="+Maxvalue+" And Long Min is ="+Minvalue);
		System.out.println("Double Max is ="+Max+" And Double Min is ="+Min);

	}

}
